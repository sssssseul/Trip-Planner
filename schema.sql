CREATE TABLE IF NOT EXISTS trips (
  id SERIAL PRIMARY KEY,
  title TEXT NOT NULL DEFAULT '나의 여행',
  start_date TEXT NOT NULL,
  end_date TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS checklist_items (
  id SERIAL PRIMARY KEY,
  trip_id INTEGER NOT NULL REFERENCES trips(id) ON DELETE CASCADE,
  text TEXT NOT NULL,
  done BOOLEAN NOT NULL DEFAULT false,
  sort_order INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS day_events (
  id SERIAL PRIMARY KEY,
  trip_id INTEGER NOT NULL REFERENCES trips(id) ON DELETE CASCADE,
  event_date TEXT NOT NULL,
  main_event TEXT NOT NULL DEFAULT '',
  UNIQUE(trip_id, event_date)
);

CREATE TABLE IF NOT EXISTS itinerary_items (
  id SERIAL PRIMARY KEY,
  trip_id INTEGER NOT NULL REFERENCES trips(id) ON DELETE CASCADE,
  event_date TEXT NOT NULL,
  time TEXT DEFAULT '',
  text TEXT NOT NULL,
  transport BOOLEAN NOT NULL DEFAULT false,
  sort_order INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE itinerary_items ADD COLUMN IF NOT EXISTS end_time TEXT DEFAULT '';
ALTER TABLE itinerary_items ADD COLUMN IF NOT EXISTS note TEXT DEFAULT '';
ALTER TABLE trips ADD COLUMN IF NOT EXISTS description TEXT DEFAULT '';
